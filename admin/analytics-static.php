	(
		SELECT
			transaction_items.customer_phonenumber AS phone,
			SUM(transaction_items.amount) AS total
		FROM
			customers,
			transaction_items,
			eligible_skus
		WHERE
			customers.mobilenumber=transaction_items.customer_phonenumber AND
			transaction_items.plucode=eligible_skus.plucode AND
			transaction_items.media_line_number=1
		GROUP BY
			transaction_items.customer_phonenumber
	) AS totals
WHERE
	total>100
ORDER BY
	total
		DESC
